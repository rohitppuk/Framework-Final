package com.homework.JavaWeek2;

public class ArithmeticOperation {
    public static void main(String[] args) {
        int x = 10; int a =10;
        int y =20;  int b =10;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        a++; // a =a +1
        System.out.println(a);
        b--; // b= b -1
        System.out.println(b);
    }
}
